        {
            using (var dbobj = new MvcProjectDBEntities())
            {
                var roles = (from user in dbobj.Employees
                             join role in dbobj.Roletbs on user.RoletbId equals role.RoletbId
                             where user.EmpUserName == username
                             select role.RollName).ToArray();
                return roles;
            }
